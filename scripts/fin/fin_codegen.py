def gen_menu(m: dict) -> None:
    rows = []
    root_sort = m.get("menu_sort_order", 1.5)

    def emit(node, parent_code, sort):
        item = {"code": node["code"], "name": node["name"]}
        if parent_code:
            item["parentCode"] = parent_code
        if node.get("path"):
            item["path"] = node["path"]
        if node.get("icon"):
            item["icon"] = node["icon"]
        item["sortOrder"] = sort
        rows.append(item)
        for i, child in enumerate(node.get("children", [])):
            emit(child, node["code"], i)

    for top in m["menus"]:
        item = {"code": top["code"], "name": top["name"], "icon": top.get("icon", "Money"),
                "sortOrder": root_sort}
        rows.append(item)
        for i, child in enumerate(top.get("children", [])):
            emit(child, top["code"], i)

    body = ",\n".join("  " + _ts_obj(r) for r in rows)
    content = (GEN_TS
               + "import type { MenuConfigItem } from './menuConfig'\n\n"
               + "/** FIN（経理・原価・人事）メニュー定義。menuConfig.ts から spread される。 */\n"
               + "export const finMenuConfig: MenuConfigItem[] = [\n"
               + body + ",\n]\n")
    _write(ROOT / "frontend" / "src" / "router" / "finMenuConfig.ts", content)


def _ts_obj(d: dict) -> str:
    parts = []
    for k, v in d.items():
        if isinstance(v, str):
            parts.append(f"{k}: '{v}'")
        else:
            parts.append(f"{k}: {v}")
    return "{ " + ", ".join(parts) + " }"


def gen_router(m: dict) -> None:
    lines = []
    for leaf, chain in _menu_leaves(m):
        cname = comp_name(leaf["code"])
        folder = leaf.get("domain") or "home"
        route_path = leaf["path"].lstrip("/")
        lines.append(
            f"  {{ path: '{route_path}', name: '{cname}', "
            f"component: () => import('@/views/fin/{folder}/{cname}.vue'), "
            f"meta: {{ title: '{leaf['name']}', group: 'FIN', requiresAuth: true }} }},"
        )
    # redirects
    for r in m.get("redirects", []):
        lines.append(f"  {{ path: '{r['from'].lstrip('/')}', redirect: '{r['to']}' }},")
    content = (GEN_TS
               + "import type { RouteRecordRaw } from 'vue-router'\n\n"
               + "/** FIN（経理・原価・人事）ルート定義。shared/router/index.ts の MainLayout children に spread される。 */\n"
               + "export const finRoutes: RouteRecordRaw[] = [\n"
               + "\n".join(lines) + "\n]\n")
    _write(ROOT / "frontend" / "src" / "shared" / "router" / "finRoutes.ts", content)


def gen_locales(m: dict) -> None:
    # menu.<CODE> を各言語で
    langs = {"ja": "name", "zh": "name_zh", "en": "name_en", "vi": "name_en"}
    collected = []

    def walk(nodes):
        for n in nodes:
            collected.append(n)
            if n.get("children"):
                walk(n["children"])
    walk(m["menus"])

    locdir = ROOT / "frontend" / "src" / "locales" / "fin"
    for lang, key in langs.items():
        menu_lines = []
        for n in collected:
            label = n.get(key) or n.get("name")
            menu_lines.append(f"    {n['code']}: '{label}',")
        content = (GEN_TS
                   + f"/** FIN locale fragment ({lang})。i18n.ts でベース messages にマージされる。 */\n"
                   + "export default {\n"
                   + "  menu: {\n"
                   + "\n".join(menu_lines) + "\n"
                   + "  },\n"
                   + "  fin: {\n"
                   + "    moduleName: '" + ("経理・原価・人事" if lang == "ja" else "财务·成本·人事" if lang == "zh" else "Finance, Costing & HR") + "',\n"
                   + "  },\n"
                   + "}\n")
        _write(locdir / f"{lang}.ts", content)

    # index.ts
    idx = (GEN_TS
           + "import ja from './ja'\nimport zh from './zh'\nimport en from './en'\nimport vi from './vi'\n\n"
           + "export const finLocales = { ja, zh, en, vi }\n")
    _write(locdir / "index.ts", idx)


