def _write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    print(f"  wrote {path.relative_to(ROOT)}")


def main():
    ap = argparse.ArgumentParser(description="FIN code generator")
    ap.add_argument("--target", default="all",
                    help="all|db|backend|frontend|menu|router|locales（カンマ区切り可）")
    args = ap.parse_args()
    m = yaml.safe_load(MANIFEST.read_text(encoding="utf-8"))

    targets = args.target.split(",")
    if "all" in targets:
        targets = ["db", "backend", "frontend", "menu", "router", "locales"]

    dispatch = {
        "db": gen_db,
        "backend": gen_backend,
        "frontend": gen_frontend,
        "menu": gen_menu,
        "router": gen_router,
        "locales": gen_locales,
    }
    for t in targets:
        t = t.strip()
        if t not in dispatch:
            raise SystemExit(f"unknown target: {t}")
        print(f"[fin_codegen] target={t}")
        dispatch[t](m)
    print("[fin_codegen] done.")


if __name__ == "__main__":
    main()
