def _backend_models(domain: dict) -> str:
    out = [GEN_PY,
           f'"""{domain.get("label","")}（{domain["id"]}）SQLAlchemy モデル。"""',
           "from sqlalchemy import (Column, Integer, BigInteger, String, Text, Date, DateTime,",
           "                        Boolean, ForeignKey)",
           "from sqlalchemy.types import Numeric",
           "from sqlalchemy.sql import func",
           "",
           "from app.core.database import Base",
           "",
           ""]
    for ent, parent in iter_entities(domain):
        out.append(f"class {ent['name']}(Base):")
        out.append(f'    __tablename__ = "{ent["table"]}"')
        out.append("")
        out.append("    id = Column(BigInteger, primary_key=True, autoincrement=True)")
        for c in entity_columns(ent, parent):
            args = [sa_type(c)]
            if c.get("fk"):
                ref = c["fk"]
                od = c.get("on_delete", "SET NULL")
                args.append(f'ForeignKey("{ref}", ondelete="{od}")')
            if not c.get("nullable", True):
                args.append("nullable=False")
            if c.get("index") or c.get("unique"):
                args.append("index=True")
            if c.get("unique"):
                args.append("unique=True")
            if "default" in c:
                dv = c["default"]
                if c["type"] == "bool":
                    args.append(f"default={'True' if dv in (True,'true',1) else 'False'}")
                elif c["type"] in ("int", "bigint", "dec"):
                    args.append(f"default={dv}")
                else:
                    args.append(f'default="{dv}"')
            out.append(f"    {c['name']} = Column({', '.join(args)})")
        out.append("    created_by = Column(String(100))")
        out.append("    updated_by = Column(String(100))")
        out.append("    created_at = Column(DateTime, server_default=func.now())")
        out.append("    updated_at = Column(DateTime, server_default=func.now(), onupdate=func.now())")
        out.append("")
        out.append("")
    return "\n".join(out)


