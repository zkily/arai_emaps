def resource_slug(table: str) -> str:
    """fin_journal_entry -> journal-entry"""
    name = table[4:] if table.startswith("fin_") else table
    return name.replace("_", "-")


def comp_name(menu_code: str) -> str:
    """FIN_ACCT_JOURNAL -> FinAcctJournal"""
    return "".join(p.capitalize() for p in menu_code.split("_"))


def iter_entities(domain: dict):
    """親 + 子をフラットに列挙。子には parent 情報を付与。"""
    for ent in domain.get("entities", []):
        yield ent, None
        for child in ent.get("children", []):
            yield child, ent


def entity_columns(ent: dict, parent: dict | None):
    """parent_fk を含む全列を返す。"""
    cols = []
    if parent is not None:
        cols.append({
            "name": ent["parent_fk"],
            "type": "bigint",
            "fk": parent["table"] + ".id",
            "on_delete": "CASCADE",
            "nullable": False,
            "comment": "親ID",
        })
    cols.extend(ent.get("columns", []))
    return cols


def editable_columns(ent: dict, parent: dict | None):
    return entity_columns(ent, parent)


