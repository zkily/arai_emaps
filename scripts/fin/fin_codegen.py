def _label(col: dict) -> str:
    """技術的な enum コメント（'a/b/c'）や FK 参照（'x.id'）はラベルに使わず、
    既知名マップ or カラム名を人間可読化する。"""
    name = col["name"]
    if name in _LABEL_MAP:
        return _LABEL_MAP[name]
    comment = col.get("comment", "")
    if comment and "/" not in comment and "." not in comment:
        return comment
    if name.endswith("_id"):
        base = name[:-3]
        return _LABEL_MAP.get(base, base.replace("_", " "))
    return name.replace("_", " ")


