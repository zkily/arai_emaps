def _menu_leaves(m: dict):
    """path を持つ leaf を (leaf, parent_chain) で列挙。"""
    result = []

    def walk(nodes, chain):
        for n in nodes:
            if n.get("path") and n.get("view"):
                result.append((n, chain))
            if n.get("children"):
                walk(n["children"], chain + [n])
    walk(m["menus"], [])
    return result


