def sa_type(col: dict) -> str:
    t = col["type"]
    if t == "str":
        return f"String({col.get('len', 255)})"
    if t == "text":
        return "Text"
    if t == "int":
        return "Integer"
    if t == "bigint":
        return "BigInteger"
    if t == "dec":
        return f"Numeric({col.get('p', 18)}, {col.get('s', 2)})"
    if t == "date":
        return "Date"
    if t == "datetime":
        return "DateTime"
    if t == "bool":
        return "Boolean"
    raise ValueError(f"unknown type {t}")


def sql_type(col: dict) -> str:
    # fin_* テーブルの id は BIGINT。FK 参照列も同一型に揃える（MySQL 3780 回避）
    if col.get("fk") and str(col["fk"]).endswith(".id"):
        return "BIGINT"
    t = col["type"]
    if t == "str":
        return f"VARCHAR({col.get('len', 255)})"
    if t == "text":
        return "TEXT"
    if t == "int":
        return "INT"
    if t == "bigint":
        return "BIGINT"
    if t == "dec":
        return f"DECIMAL({col.get('p', 18)}, {col.get('s', 2)})"
    if t == "date":
        return "DATE"
    if t == "datetime":
        return "DATETIME"
    if t == "bool":
        return "TINYINT(1)"
    raise ValueError(f"unknown type {t}")


def py_type(col: dict) -> str:
    t = col["type"]
    if t in ("str", "text"):
        return "str"
    if t in ("int", "bigint"):
        return "int"
    if t == "dec":
        return "float"
    if t == "date":
        return "date"
    if t == "datetime":
        return "datetime"
    if t == "bool":
        return "bool"
    raise ValueError(f"unknown type {t}")


def ts_field_type(col: dict) -> str:
    t = col["type"]
    if t == "bool":
        return "switch"
    if t in ("int", "bigint", "dec"):
        return "number"
    if t == "date":
        return "date"
    if t == "datetime":
        return "datetime"
    return "text"


