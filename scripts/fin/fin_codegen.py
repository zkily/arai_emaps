def _ja_sql_comment(col: dict) -> str:
    """列 COMMENT を日本語に正規化する。"""
    raw = (col.get("comment") or "").strip()
    if raw in _COMMENT_ENUM_JA:
        return _COMMENT_ENUM_JA[raw]
    if raw and "/" not in raw and "." not in raw and not raw.isascii():
        return raw
    if raw:
        # 英語のみの短い列挙はそのまま括弧付きで日本語ラベルを付与
        if "/" in raw or raw.endswith(" 等"):
            return _COMMENT_ENUM_JA.get(raw, f"値（{raw}）")
        if "." in raw:
            return _COMMENT_ENUM_JA.get(raw, raw.replace(".", "の").replace("_", " "))
    name = col["name"]
    if name in _COL_NAME_JA:
        return _COL_NAME_JA[name]
    if name.endswith("_id"):
        base = name[:-3].replace("_", " ")
        return f"{base}ID"
    if name.endswith("_at"):
        return f"{name[:-3].replace('_', ' ')}日時"
    if name.endswith("_date"):
        return f"{name[:-5].replace('_', ' ')}日"
    if name.endswith("_no"):
        return f"{name[:-3].replace('_', ' ')}番号"
    if name.endswith("_json"):
        return f"{name[:-5].replace('_', ' ')}（JSON）"
    if raw:
        return raw
    return name.replace("_", " ")


def _ja_table_comment(table: str, ent: dict, domain: dict) -> str:
    return _TABLE_COMMENT_JA.get(table, ent.get("comment") or domain.get("label", table))


# --------------------------------------------------------------------------- #
# DB 生成
# --------------------------------------------------------------------------- #
def gen_db(m: dict) -> None:
    out = [GEN_SQL,
           "-- =============================================================================",
           "-- FIN（経理・原価・人事）全量スキーマ  (manifest version {})".format(m.get("version")),
           "-- 実行: 02_baseline_full_schema.sql の後。新規DBは bootstrap_full_database.py 推奨。",
           "-- =============================================================================",
           "SET NAMES utf8mb4;",
           ""]

    for domain in m["domains"]:
        out.append(f"-- ------- ドメイン: {domain['id']}（{domain.get('label','')}） -------")
        for ent, parent in iter_entities(domain):
            table = ent["table"]
            cols = entity_columns(ent, parent)
            lines = [f"CREATE TABLE IF NOT EXISTS `{table}` (",
                     "  `id` BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT 'ID',"]
            idx_lines = []
            fk_lines = []
            for c in cols:
                nn = "" if c.get("nullable", True) else " NOT NULL"
                default = ""
                if "default" in c:
                    dv = c["default"]
                    if c["type"] == "bool":
                        default = f" DEFAULT {1 if dv in (True, 'true', 1) else 0}"
                    elif c["type"] in ("int", "bigint", "dec"):
                        default = f" DEFAULT {dv}"
                    else:
                        default = f" DEFAULT '{dv}'"
                ja = _ja_sql_comment(c).replace("'", "''")
                cm = f" COMMENT '{ja}'"
                lines.append(f"  `{c['name']}` {sql_type(c)}{nn}{default}{cm},")
                if c.get("unique"):
                    idx_lines.append(f"  UNIQUE KEY `uq_{table}_{c['name']}` (`{c['name']}`),")
                elif c.get("index") or c.get("fk"):
                    idx_lines.append(f"  KEY `idx_{table}_{c['name']}` (`{c['name']}`),")
                if c.get("fk"):
                    ref_table, ref_col = c["fk"].split(".")
                    od = c.get("on_delete", "SET NULL")
                    fk_lines.append(
                        f"  CONSTRAINT `fk_{table}_{c['name']}` FOREIGN KEY (`{c['name']}`) "
                        f"REFERENCES `{ref_table}` (`{ref_col}`) ON DELETE {od},"
                    )
            lines.append("  `created_by` VARCHAR(100) DEFAULT NULL COMMENT '作成者',")
            lines.append("  `updated_by` VARCHAR(100) DEFAULT NULL COMMENT '更新者',")
            lines.append("  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '作成日時',")
            lines.append("  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新日時',")
            body = lines + idx_lines + fk_lines
            # 末尾カンマ除去
            body[-1] = body[-1].rstrip(",")
            tbl_comment = _ja_table_comment(table, ent, domain).replace("'", "''")
            body.append(f") ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='{tbl_comment}';")
            out.extend(body)
            out.append("")
    out.append(_seed_sql())
    # 既存 migrations は 02..35 を使用済み。04 は cutting と衝突するため次の空き番号 36 を採番。
    _write(ROOT / "backend" / "database" / "migrations" / "36_fin_full_schema.sql", "\n".join(out))


