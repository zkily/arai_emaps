def _first_path(node: dict):
    if node.get("path"):
        return node["path"]
    for c in node.get("children", []):
        p = _first_path(c)
        if p:
            return p
    return "/fin"


def _parent_code(nodes, target, parent=None):
    for n in nodes:
        if n["code"] == target:
            return parent
        if n.get("children"):
            r = _parent_code(n["children"], target, n["code"])
            if r:
                return r
    return None


